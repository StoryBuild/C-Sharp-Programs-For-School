int age = int.Parse(Console.ReadLine());
if (age <= 3)
{
    Console.WriteLine("baby");
}
else if (age <= 10)
{
    Console.WriteLine("child");
}
else if (age <= 19)
{
    Console.WriteLine("teenager");
}
else if (age <= 30)
{
    Console.WriteLine("young");
}
else if (age <= 65)
{
    Console.WriteLine("adult");
}
else if (age > 65)
{
    Console.WriteLine("old");
}




